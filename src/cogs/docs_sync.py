class DocsSyncCog(commands.Cog):
    """Cog that handles automated documentation sync triggered by Discord webhooks."""

    def __init__(
        self,
        bot: commands.Bot,
        runner: ClaudeRunner,
        channel_id: int,
    ) -> None:
        self.bot = bot
        self.runner = runner
        self.channel_id = channel_id
        self._lock = asyncio.Lock()

    @commands.Cog.listener()
    async def on_message(self, message: discord.Message) -> None:
        """Handle webhook trigger messages."""
        # Only respond to webhook messages (not regular users, not bots)
        if not message.webhook_id:
            return

        # Only in the configured channel
        if message.channel.id != self.channel_id:
            return

        # Only respond to the specific trigger prefix
        if not message.content.startswith(TRIGGER_PREFIX):
            return

        logger.info(f"docs-sync ãƒˆãƒªã‚¬ãƒ¼å—ä¿¡: {message.content}")

        # Prevent concurrent runs
        if self._lock.locked():
            await message.reply("â³ docs-sync ã¯æ—¢ã«å®Ÿè¡Œä¸­ã§ã™ã€‚ã‚¹ã‚­ãƒƒãƒ—ã—ã¾ã™ã€‚")
            return

        async with self._lock:
            await self._run_docs_sync(message)

    async def _run_docs_sync(self, trigger_message: discord.Message) -> None:
        """Run the docs sync process via Claude Code."""
        thread = await trigger_message.create_thread(name="ğŸ“„ docs-sync")
